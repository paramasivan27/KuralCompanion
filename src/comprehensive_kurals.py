def get_additional_kurals():
    """Get all additional kurals"""
    all_additional = []
    for theme, kurals in ADDITIONAL_KURALS.items():
        all_additional.extend(kurals)
    return all_additional

def merge_kural_databases(main_db, additional_db):
    """Merge the main database with additional kurals"""
    merged_db = main_db.copy()
    
    for theme, kurals in additional_db.items():
        if theme in merged_db:
            merged_db[theme].extend(kurals)
        else:
            merged_db[theme] = kurals
    
    return merged_db 