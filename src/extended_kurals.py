def merge_all_kural_databases(main_db, additional_db, extended_db):
    """Merge all three databases for maximum coverage"""
    merged_db = main_db.copy()
    
    # Merge additional kurals
    for theme, kurals in additional_db.items():
        if theme in merged_db:
            merged_db[theme].extend(kurals)
        else:
            merged_db[theme] = kurals
    
    # Merge extended kurals
    for theme, kurals in extended_db.items():
        if theme in merged_db:
            merged_db[theme].extend(kurals)
        else:
            merged_db[theme] = kurals
    
    return merged_db 